# PYTHON SLICING
# slicing strings
'''
b(start , end-1)
'''

b = "Hello, World!"
print(b[2:5])

# Slice From the Start
print(b[:5])

# Slice To the End
b = "Hello, World!"
print(b[2:])

# Negative Indexing , Use negative indexes to start the slice from the end of the string:
b = "Hello, World!"
print(b[-5:-2]) # orl
