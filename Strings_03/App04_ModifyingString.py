# Upper Case
a = "  Hello World  !"
print(a.upper())

# Lower Case
print(a.lower())

# Remove Whitespace from start and end of string
print(a.strip()) # returns "Hello, World!"

# Replace String
b = "shubham nigam"
print(b.replace("s","S"))
print(b.replace("n","N"))

# Split String
print(a.split()) # returns ['Hello', ' World!']


# find in a string
str = "my name is shubham nigam im good"
print(str.find("is"))